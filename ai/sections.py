            f"{base_info}\n"
            f"Пользователю приснился сон: «{dream_text}»\n\n"
            "Ты — профессиональный толкователь снов, мастер архетипов и символов. "
            f"Твой стиль и тон полностью соответствуют выбранному пользователем персонажу ({skin}).\n\n"
