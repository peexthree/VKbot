async def extract_tags(text: str) -> list[str]:
    prompt = (
        f"Проанализируй следующий эзотерический разбор или ответ: '{text}'. "
        f"Выдели 1-3 главных тега (фокуса), которые описывают состояние и запросы пользователя. "
        f"Примеры тегов: 'гармония-в-финансах', 'поиск-любви', 'внутренний-свет', 'новые-горизонты', 'исцеление-сердца', 'путь-к-себе'. "
        f"Верни строго JSON-список строк, например: [\"гармония-в-финансах\", \"исцеление-сердца\"]."
    )
    res = await generate_text(prompt, json_mode=True)
    if not res:
        return []
    try:
        clean = clean_ai_json(res)
        data = json.loads(clean, strict=False)
        if isinstance(data, list):
            return data
        elif isinstance(data, dict):
            # Если ИИ вернул объект вместо списка, ищем список внутри
            for val in data.values():
                if isinstance(val, list):
                    return val
        return []
    except Exception as e:
        logger.error(f"Ошибка парсинга тегов: {e}. Raw: {res}")
        return []

async def extract_birth_data(text: str) -> dict | None:
    prompt = (
        f"Пользователь написал: '{text}'. "
        f"Вытащи дату рождения (DD.MM.YYYY), время (HH:MM) и город. "
        f"Если пользователь не указал время, по умолчанию ставь 12:00. Время всегда приводи к формату HH:MM. "
        f"Верни строго JSON: {{\"date\": \"15.04.1990\", \"time\": \"14:30\", \"city\": \"Москва\"}}."
    )
    res = await generate_text(prompt, json_mode=True)
    if not res:
        return None
    try:
        clean = clean_ai_json(res)
        return json.loads(clean, strict=False)
    except Exception as e:
        logger.error(f"Failed to parse birth data JSON: {e}. Raw: {res}")
        return None

